package resources;

/**
 * A class that serves as a container for the values of all possible
 * cell shapes. The purpose of this class is to avoid hard-coding these
 * values in our code--instead, we can reference the number of cell sides using 
 * this class
 * 
 * 
 * @author matthewfaw
 *
 */
public final class CellShapes {
	public static final int RECTANGULAR = 4;
	public static final int HEXAGONAL = 6;
	public static final int TRIANGULAR = 3;
	public static final int DEFAULT = 0;
}
